var util      = require('util')
  ;

var BaseBolt  = require('./base_bolt')
  ;

util.inherits(Bolt, BaseBolt);

/* ========================================================================== *
 *  PUBLIC FUNCTIONS                                                          *
 * ========================================================================== */
Bolt.prototype.write = function(data) {
  this.emit('data', data);
};

/* ========================================================================== *
 *  CONSTRUCTOR & INITIALIZATION                                              *
 * ========================================================================== */
function Bolt() {
  BaseBolt.apply(this);
}

/* ========================================================================== */
/* ========================================================================== */
module.exports = Bolt;
